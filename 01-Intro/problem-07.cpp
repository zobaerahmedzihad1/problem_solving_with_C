/* Program that will receive the values of an integer, a floating point number, a character from
the keyboard and print those values.
*/
#include<bits/stdc++.h>
using namespace std;
int main()
{
    int num1;
    double num2;
    char ch;
    
    cin>>num1>>num2>>ch;
    cout<<"The integer value : "<<num1<<endl;
    cout<<"The floating point value is : "<<num2<<endl;
    cout<<"The character value : "<<ch;
    
    return 0;
}